#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char		*prekol;
	size_t				i;

	i = 0;
	prekol = s;
	while (i < n)
	{
		prekol[i] = '\0';
		i++;
	}
}
